const ITEM_DAY_GROUP: Record<string, FarmDayGroup> = Object.fromEntries([
  ...MON_THU_ITEMS.map((item) => [item, 'monThu'] as const),
  ...TUE_FRI_ITEMS.map((item) => [item, 'tueFri'] as const),
  ...WED_SAT_ITEMS.map((item) => [item, 'wedSat'] as const),
])

/** Returns which day group (Mon/Thu, Tue/Fri, Wed/Sat) a domain-farmed item key drops on, or `undefined` for items not gated by a domain weekday rotation (e.g. mob drops, local specialties, Mora). */
export function getFarmDayGroup(itemKey: string): FarmDayGroup | undefined {
  return ITEM_DAY_GROUP[itemKey]
}
