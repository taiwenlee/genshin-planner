/**
 * Talent books and weapon ascension gems are only dropped by their source
 * domain on specific days — each domain offers 3 material families that
 * rotate Mon/Thu, Tue/Fri, Wed/Sat respectively, with every family available
 * on Sunday. The grouping below is keyed by the exact item key produced by
 * `nameToKey` in the gen-mats pipeline (matches `allCharacterMats_gen.json`
 * and `allWeaponMats_gen.json` item names byte-for-byte), so it stays
 * correct without re-deriving the family from the item's display name.
 */
export type FarmDayGroup = 'monThu' | 'tueFri' | 'wedSat'

export const FARM_DAY_GROUP_LABEL: Record<FarmDayGroup, string> = {
  monThu: 'Mon/Thu',
  tueFri: 'Tue/Fri',
  wedSat: 'Wed/Sat',
}

